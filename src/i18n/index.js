// Detect user language
export function detectLanguage() {
  // Check localStorage first
  const saved = localStorage.getItem('markdev-language');
  if (saved && (saved === 'en' || saved === 'pt')) {
    return saved;
  }
  
  // Check navigator language
  const browserLang = navigator.language || navigator.languages?.[0] || 'en';
  const langCode = browserLang.toLowerCase();
  
  // Return 'pt' for Portuguese variants, 'en' for everything else
  return langCode.startsWith('pt') ? 'pt' : 'en';
}

// Save language preference
export function saveLanguage(lang) {
  localStorage.setItem('markdev-language', lang);
}

// Get translation
export function t(key, lang = null) {
  const currentLang = lang || detectLanguage();
  return translations[currentLang]?.[key] || translations.en[key] || key;
}

// Get all translations for a language
export function getTranslations(lang = null) {
  const currentLang = lang || detectLanguage();
  return translations[currentLang] || translations.en;
}